    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_searchButtonActionPerformed
        try {
            smt = conn.createStatement();
            // String sClass = stdClass.getText();
            // String studentRoll = stdRoll.getText();
            String sClass = (String) classCombo.getSelectedItem();
            String studentRoll = (String) rollCombo.getSelectedItem();

            String sql = "select * from student where stdRoll='" + studentRoll + "' and class='" + sClass + "'";
            rs = smt.executeQuery(sql);

            if (rs.next()) {
                stdName.setText(rs.getString("stdName"));
                studentGuardian.setText(rs.getString("stdGuardian"));
                studentDOB.setText(rs.getString("stdDOB"));
                studentGender.setText(rs.getString("gender"));
                studentContact.setText(rs.getString("phone"));
                studentEmail.setText(rs.getString("email"));
                address.setText(rs.getString("address"));
                studentClass.setText(rs.getString("class"));
                String fee = String.valueOf(rs.getFloat("fee"));
                studentFee.setText(fee);
                studentRollNo.setText(rs.getString("stdRoll"));
            } else {
                JOptionPane.showMessageDialog(null, "No data found!");
            }
            String imageId = sClass + studentRoll;
            sql = "Select * from image WHERE id='" + imageId + "'";
            rs = smt.executeQuery(sql);
            // rs.next();
            if (rs.next()) {
                byte[] imageData = rs.getBytes("imageFile");
                ImageIcon format = new ImageIcon(imageData);
                Image mm = format.getImage();
                Image img2 = mm.getScaledInstance(200, 200, Image.SCALE_SMOOTH);
                // ImageIcon img3 = new ImageIcon(img2);
                imageLabel.setIcon(new ImageIcon(img2));
            } else {
                ImageIcon i = new ImageIcon(getClass().getResource("/icons/Default.png"));
                Image m = i.getImage();
                Image i2 = m.getScaledInstance(200, 200, Image.SCALE_SMOOTH);
                imageLabel.setIcon(new ImageIcon(i2));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }// GEN-LAST:event_searchButtonActionPerformed

