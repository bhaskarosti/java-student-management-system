    private void classComboItemStateChanged(java.awt.event.ItemEvent evt) {// GEN-FIRST:event_classComboItemStateChanged
        try {
            rollCombo.removeAllItems();
            String sClass = (String) classCombo.getSelectedItem();
            Statement smt = conn.createStatement();
            ResultSet rs = smt
                    .executeQuery("select stdRoll from student where class='" + sClass + "' order by stdRoll ASC");
            // classCombo.addItem("");
            while (rs.next()) {
                rollCombo.addItem(rs.getString("stdRoll"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }// GEN-LAST:event_classComboItemStateChanged

